    private void importMapSprites() {
        BufferedImage image = Load.getImage(Load.mapTiles);

        levelMap = new BufferedImage[180];
        for (int i = 0; i < 9; ++i) {
            for (int j = 0; j < 20; ++j) {
                int index = i * 20 + j;
                levelMap[index] = image.getSubimage(j * 32, i * 32, 32, 32);
            }
        }
    }

